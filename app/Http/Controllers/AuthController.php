    {
        $attributes = $request->validate([
            "email" => ['required', 'email'],
            "password" => ['required'],
        ]);

        if (Auth::attempt($attributes)){
            return redirect(route('admin'));
        }

        throw ValidationException::withMessages([
            'email' => "Your Email / Password Wrong"
            
        ]);
        
    }

